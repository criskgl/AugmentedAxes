Both shapes are using the same mathematical function but with different parameters a,b,c.
</p>
<p>
we start in a point x,y,z and obtain the newx, newy, newz for a number of times(n) the following way:
</p>
<p>
PSEUDOCODE:
iterate(n){
newx= a*x*y*z
newy= b*newx*c
newz=newx+newy*c
}</p>
